def _merge_files(frontend_code: dict, backend_code: dict) -> dict[str, str]:
    merged: dict[str, str] = {}

    for path, content in backend_code.items():
        if isinstance(path, str) and isinstance(content, str):
            merged[path] = content

    for path, content in frontend_code.items():
        if not isinstance(path, str) or not isinstance(content, str):
            continue

        normalized_path = path if path.startswith("web/") else f"web/{path}"
        merged[normalized_path] = content

    return merged


def _get_deploy_blocker(
    frontend_code: dict,
    backend_code: dict,
    blueprint: dict | None,
    code_eval_result: dict | None = None,
    selected_flagship: str = "",
    build_validation: dict | None = None,
) -> str | None:
    eval_result = code_eval_result or {}
    if _allow_flagship_local_fallback(eval_result, selected_flagship):
        return None
    if eval_result.get("deployment_blocked"):
        blockers = eval_result.get("blockers") or []
        if blockers:
            return str(blockers[0])
        return "code quality gates blocked deployment"
    if eval_result and not eval_result.get("passed", False):
        return "code evaluation did not pass"

    bv = build_validation or {}
    if bv and not bv.get("passed", True) and not bv.get("skipped", False):
        errors = bv.get("errors") or ["build validation failed"]
        return f"build validation failed: {errors[0]}"

    expected_frontend = (blueprint or {}).get("frontend_files") or {}
    expected_backend = (blueprint or {}).get("backend_files") or {}

    if expected_backend:
        missing_backend = [path for path in ("main.py", "requirements.txt") if path not in backend_code]
        if missing_backend:
            return f"missing backend files: {', '.join(missing_backend)}"
        if len(backend_code) < 2:
            return "backend bundle is too small to deploy"

    if expected_frontend:
        if "package.json" not in frontend_code:
            return "missing frontend package.json"
        frontend_source_files = [
            path
            for path in frontend_code
            if path.startswith("src/") or path.startswith("app/") or path.endswith((".tsx", ".ts", ".jsx", ".js"))
        ]
        if len(frontend_code) < 3 or not frontend_source_files:
            return "frontend bundle is too small to deploy"

    if not frontend_code and not backend_code:
        return "no generated code files were available"

    return None


def _allow_flagship_local_fallback(eval_result: dict, selected_flagship: str) -> bool:
    if not selected_flagship:
        return False
    blockers = [str(value).strip() for value in eval_result.get("blockers") or [] if str(value).strip()]
    if blockers != ["deterministic fallback scaffold detected"]:
        return False
    completeness = float(eval_result.get("completeness", 0) or 0)
    runnability = float(eval_result.get("runnability", 0) or 0)
    experience = float(eval_result.get("experience", 0) or 0)
    return completeness >= 95 and runnability >= 85 and experience >= 85


