def _extract_api_endpoints(backend_code: dict) -> list[tuple[str, str]]:
    endpoints: list[tuple[str, str]] = []
    route_pattern = re.compile(r"@app\.(get|post|put|patch|delete)\(\s*['\"]([^'\"]+)['\"]")

    for content in backend_code.values():
        if not isinstance(content, str):
            continue
        for method, path in route_pattern.findall(content):
            endpoint = (method.upper(), path)
            if endpoint not in endpoints:
                endpoints.append(endpoint)

    return endpoints


def _generate_ci_yml(has_frontend: bool = False) -> str:
    backend_job = """  backend:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-python@v5
        with:
          python-version: "3.13"
          allow-prereleases: true
      - name: Install backend dependencies
        run: |
          if [ -f requirements.txt ]; then
            pip install -r requirements.txt
          else
            echo "No requirements.txt found, skipping"
          fi
      - name: Backend smoke test
        run: python -c "import ast; [ast.parse(open(f).read()) for f in __import__('glob').glob('*.py')]"
"""

    frontend_job = """
  frontend:
    runs-on: ubuntu-latest
    defaults:
      run:
        working-directory: web
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-node@v4
        with:
          node-version: "22"
      - name: Install frontend dependencies
        run: |
          if [ -f package-lock.json ]; then
            npm ci
          elif [ -f package.json ]; then
            npm install
          else
            echo "No package.json found, skipping"
            exit 0
          fi
      - name: Build frontend
        run: |
          if [ -f package.json ]; then
            npm run build
          fi
"""

    yml = """name: CI

on:
  push:
    branches: ["main", "master"]
  pull_request:

jobs:
"""
    yml += backend_job
    if has_frontend:
        yml += frontend_job
    return yml


